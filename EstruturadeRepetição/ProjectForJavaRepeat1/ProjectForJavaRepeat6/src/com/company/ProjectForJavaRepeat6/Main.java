package com.company.ProjectForJavaRepeat6;

public class Main {

    public static void main(String[] args) {
        for (int i = 1; i <= 20; i++) {
            System.out.println(i);
        }
        for (int i = 1; i <= 20; i++) {
            if (i == 20) {
                System.out.print(i);
            } else {
                System.out.print(i + ", ");
            }
        }
    }
}


